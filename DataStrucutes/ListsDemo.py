# Boolean operations
n = [1,3,4,6,8]
m = [1,3,4,6,8]
print(n is m) # "is" is used to check whether both objects are equal or not
print(n==m) # it is actually checking the values are equal or not
k = n
print(k is n)
print(len(n))
print(min(n))
print(max(n))
print(sum(n))
print(all(n))
print(any(n))
print(n+m)
print(n[3])
print(3 in n)
print(3 not in n)
